import java.util.*;
public class CF833ATheUltimateSquare {
    public static void main(String[] args) {
        Scanner fs = new Scanner(System.in);
        int testcases = fs.nextInt();
        for (int tc = 0; tc < testcases; tc++) {
            int num = fs.nextInt();
            System.out.println((num+1)/2);
        }
    }
}
