import java.util.*;
public class CF682ASpecificTastesOfAndre {
    public static void main(String[] args) {
        Scanner fs = new Scanner(System.in);
        int testcases = fs.nextInt();
        for (int tc = 0; tc < testcases; tc++) {
            int len = fs.nextInt();
            for(int i = 0; i < len; i++){
                System.out.print(len + " ");
            }
            System.out.println();
        }
    }
}
